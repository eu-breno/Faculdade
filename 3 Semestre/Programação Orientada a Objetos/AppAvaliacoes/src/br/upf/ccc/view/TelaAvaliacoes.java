    private Avaliacao criarAvaliacaoDosCampos(Avaliacao avaliacaoIgnorada) {
        Avaliacao avaliacao = new Avaliacao();
        String matricula = txtMatricula.getText();
        validarMatriculaUnica(matricula, avaliacaoIgnorada);
        avaliacao.setMatricula(matricula);
        avaliacao.setNome(txtNome.getText());
        avaliacao.setNota01(parseNotaOpcional(txtNota01.getText(), "Nota 01"));
        avaliacao.setNota02(parseNotaOpcional(txtNota02.getText(), "Nota 02"));
        avaliacao.setNota03(parseNotaOpcional(txtNota03.getText(), "Nota 03"));
        avaliacao.setNotaRecuperacao(parseNotaOpcional(txtRecuperacao.getText(), "Recuperacao"));
        avaliacao.setNotaExame(parseNotaOpcional(txtExame.getText(), "Exame"));
        validarCombinacaoNotas(avaliacao);
        return avaliacao;
    }

    private Double parseNotaObrigatoria(String valorTexto, String campo) {
        if (valorTexto == null || valorTexto.isBlank()) {
            throw new IllegalArgumentException(campo + " deve ser informada.");
        }

        try {
            return Double.valueOf(valorTexto.trim().replace(',', '.'));
        } catch (NumberFormatException ex) {
            throw new IllegalArgumentException(campo + " invalida. Informe um numero entre 0 e 10.");
        }
    }

    private Double parseNotaOpcional(String valorTexto, String campo) {
        if (valorTexto == null || valorTexto.isBlank()) {
            return null;
        }

        try {
            return Double.valueOf(valorTexto.trim().replace(',', '.'));
        } catch (NumberFormatException ex) {
            throw new IllegalArgumentException(campo + " invalida. Informe um numero entre 0 e 10.");
        }
    }

    private void validarCombinacaoNotas(Avaliacao avaliacao) {
        int totalNotasRegulares = 0;
        if (avaliacao.getNota01() != null) {
            totalNotasRegulares++;
        }
        if (avaliacao.getNota02() != null) {
            totalNotasRegulares++;
        }
        if (avaliacao.getNota03() != null) {
            totalNotasRegulares++;
        }

        boolean possuiRecuperacao = avaliacao.getNotaRecuperacao() != null;
        if (totalNotasRegulares == 3) {
            return;
        }
        if (totalNotasRegulares == 2 && possuiRecuperacao) {
            return;
        }

        throw new IllegalArgumentException("Informe 3 notas ou 2 notas com recuperacao.");
    }

    private void atualizarTabela() {
        DefaultTableModel model = (DefaultTableModel) tblAvaliacoes.getModel();
        model.setRowCount(0);

        for (Avaliacao avaliacao : avaliacoes) {
            model.addRow(new Object[]{
                avaliacao.getMatricula(),
                avaliacao.getNome(),
                formatarNota(avaliacao.getNota01()),
                formatarNota(avaliacao.getNota02()),
                formatarNota(avaliacao.getNota03()),
                formatarNota(avaliacao.getMedia()),
                avaliacao.getSituacao(),
                formatarNota(avaliacao.getNotaRecuperacao()),
                formatarNota(avaliacao.getNotaExame())
            });
        }
    }

    private String formatarNota(Double nota) {
        return nota == null ? "" : FORMATO_NOTA.format(nota);
    }

    private void limparCampos() {
        txtMatricula.setText("");
        txtNome.setText("");
        txtNota01.setText("");
        txtNota02.setText("");
        txtNota03.setText("");
        txtRecuperacao.setText("");
        txtExame.setText("");
    }

    private void validarMatriculaUnica(String matricula, Avaliacao avaliacaoIgnorada) {
        if (matricula == null || matricula.isBlank()) {
            return;
        }

        String matriculaNormalizada = matricula.trim();
        for (Avaliacao avaliacao : avaliacoes) {
            if (avaliacao == avaliacaoIgnorada) {
                continue;
            }
            if (matriculaNormalizada.equalsIgnoreCase(avaliacao.getMatricula())) {
                throw new IllegalArgumentException("Ja existe aluno com essa matricula.");
            }
        }
    }

