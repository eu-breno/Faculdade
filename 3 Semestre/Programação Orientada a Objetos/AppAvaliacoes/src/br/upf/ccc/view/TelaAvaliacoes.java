    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        try {
            int linhaSelecionada = tblAvaliacoes.getSelectedRow();
            if (linhaSelecionada < 0) {
                throw new IllegalArgumentException("Selecione uma avaliacao para alterar.");
            }

            Avaliacao avaliacao = avaliacoes.get(linhaSelecionada);
            txtMatricula.setText(avaliacao.getMatricula());
            txtNome.setText(avaliacao.getNome());
            txtNota01.setText(formatarNota(avaliacao.getNota01()));
            txtNota02.setText(formatarNota(avaliacao.getNota02()));
            txtNota03.setText(formatarNota(avaliacao.getNota03()));
            txtRecuperacao.setText(formatarNota(avaliacao.getNotaRecuperacao()));
            txtExame.setText(formatarNota(avaliacao.getNotaExame()));
            indiceEmEdicao = linhaSelecionada;
            JOptionPane.showMessageDialog(this, "Valores prontos para alterar.");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

