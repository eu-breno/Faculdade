    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        try {
            int linhaSelecionada = tblAvaliacoes.getSelectedRow();
            if (linhaSelecionada < 0) {
                throw new IllegalArgumentException("Selecione uma avaliacao para excluir.");
            }
            avaliacoes.remove(linhaSelecionada);
            atualizarTabela();
            limparCampos();
            indiceEmEdicao = null;
            JOptionPane.showMessageDialog(this, "Avaliacao excluida com sucesso.");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
        try {
            Avaliacao avaliacaoIgnorada = indiceEmEdicao == null ? null : avaliacoes.get(indiceEmEdicao);
            Avaliacao avaliacao = criarAvaliacaoDosCampos(avaliacaoIgnorada);
            if (indiceEmEdicao == null) {
                avaliacoes.add(avaliacao);
                JOptionPane.showMessageDialog(this, "Avaliacao inserida com sucesso.");
            } else {
                avaliacoes.set(indiceEmEdicao, avaliacao);
                JOptionPane.showMessageDialog(this, "Avaliacao alterada com sucesso.");
            }
            limparCampos();
            indiceEmEdicao = null;
            tblAvaliacoes.clearSelection();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnInserirActionPerformed

